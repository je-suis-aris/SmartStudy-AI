/**
 * Populates the SmartStudy AI database with a deterministic experimental
 * corpus that the {@link ExperimentRunner} will measure.
 *
 * <p>The seeder is the analogue of the seeded candidates / jobs in the
 * Recruitlyze experiment harness: it only provides <em>inputs</em>. Every
 * number reported by the paper is computed by the real DAOs / services
 * over the data this seeder writes — nothing is generated by Math.random
 * at measurement time.
 *
 * <p>The seeder is idempotent: it tags itself with the description
 * {@code EXP_SEED} on the user rows so a second run wipes the previous
 * experimental users (and cascades into their materials / quizzes /
 * mistakes / flashcards) before re-creating them.
 *
 * <p>Run with:
 * <pre>
 *   mvn -q exec:java -Dexec.mainClass=com.smartstudy.experiments.ExperimentSeeder
 * </pre>
 *
 * <p>Optional environment variables:
 * <ul>
 *   <li>{@code EXP_STUDENTS} — number of student accounts to seed
 *       (default: 25).</li>
 *   <li>{@code EXP_WEEKS} — number of weeks of simulated quiz history
 *       (default: 8).</li>
 *   <li>{@code EXP_SEED} — seed for the deterministic skill / attempt
 *       generator (default: 20260606L).</li>
 * </ul>
 */
