    private record DisciplineKey(int courseId, int disciplineId) {}

    private Map<Integer, List<DisciplineKey>> createCatalogue(Connection c,
            int adminId, List<Integer> studentIds) throws SQLException {
        System.out.println("[seed] inserting course catalogue…");
        Map<Integer, List<DisciplineKey>> map = new LinkedHashMap<>();
        for (CourseSpec cs : CATALOGUE) {
            int courseId = insertCourse(c, adminId, cs);
            List<DisciplineKey> disciplines = new ArrayList<>();
            for (DisciplineSpec ds : cs.disciplines()) {
                int discId = insertDiscipline(c, courseId, ds);
                disciplines.add(new DisciplineKey(courseId, discId));
                for (MaterialSpec ms : ds.materials()) {
                    int matId = insertMaterial(c, studentIds.get(0), courseId, discId, ms);
                    for (QuestionSpec qs : ms.questions()) {
                        insertQuestion(c, courseId, discId, matId, qs);
                    }
                    // Flashcards are inserted for every student to feed
                    // FlashcardDAO.findByUser-based experiments.
                    for (Integer sid : studentIds) {
                        for (FlashcardSpec fs : ms.cards()) {
                            insertFlashcard(c, sid, courseId, matId, fs, "AI_SEED");
                        }
                    }
                }
            }
            map.put(courseId, disciplines);
        }
        return map;
    }

