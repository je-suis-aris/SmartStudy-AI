    /** Deterministic [0,1) pseudo-random derived from a long seed via splitmix64. */
    private static double fractional(long seed) {
        long z = seed + 0x9E3779B97F4A7C15L;
        z = (z ^ (z >>> 30)) * 0xBF58476D1CE4E5B9L;
        z = (z ^ (z >>> 27)) * 0x94D049BB133111EBL;
        z = z ^ (z >>> 31);
        return (z >>> 11) / (double) (1L << 53);
    }

    private static long mixSeed(int userId, int week, int courseId) {
        long h = RNG_SEED;
        h = h * 1315423911L + userId;
        h = h * 1315423911L + week;
        h = h * 1315423911L + courseId;
        return h;
    }

    private static int[] shuffleIndices(int n, Random rng) {
        int[] idx = new int[n];
        for (int i = 0; i < n; i++) idx[i] = i;
        for (int i = n - 1; i > 0; i--) {
            int j = rng.nextInt(i + 1);
            int t = idx[i]; idx[i] = idx[j]; idx[j] = t;
        }
        return idx;
    }

