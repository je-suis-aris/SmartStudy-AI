    private void run() throws Exception {
        System.out.println("[seed] SmartStudy AI experimental corpus seeder");
        System.out.println("[seed] students=" + DEFAULT_STUDENTS
                + " weeks=" + DEFAULT_WEEKS + " seed=" + RNG_SEED);

        try (Connection c = DBConnection.getConnection()) {
            c.setAutoCommit(false);
            try {
                wipePreviousSeed(c);
                int adminId = ensureAdmin(c);
                List<Integer> studentIds = createStudents(c, DEFAULT_STUDENTS);
                Map<Integer, List<DisciplineKey>> courseDisciplines =
                        createCatalogue(c, adminId, studentIds);
                List<Integer> materialIds = collectMaterialIds(c);
                simulateQuizHistory(c, studentIds, courseDisciplines, materialIds);
                c.commit();
            } catch (Exception ex) {
                c.rollback();
                throw ex;
            }
        }

        System.out.println("[seed] done.");
    }

